# 2. Soal Terkait Assigment (Operator Penugasan)
a = 10 # 1. Variabel utama
a += 5 # 2. Variabel utama ditimpa dan ditambah dengan value
       #    Operasi matematika terjadi pertambahan a = 10 + 5 = 15
a *= 2 # 3. Hasil pertambahan nilai variabel sebelumnya akan mengkalikan angkanya 15 * 2 = 30

print(f"Hasil Var Assigment = {a}")
